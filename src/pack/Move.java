package pack;

public class Move {
ChessPiece c;
int x;
int y;
int i;
int j;
public Move(ChessPiece c, int i, int j) {
	super();
	this.c = c;
	this.x = c.getX();
	this.y =c.getY();
}
public ChessPiece getC() {
	return c;
}
public int getX() {
	return x;
}
public int getY() {
	return y;
}
public int getI() {
	return i;
}
public int getJ() {
	return j;
}
	
}
